let color = "Black";
switch (color) {
    case "Black":
        console.log("Fly");
        break;
    case "Red":
        console.log("Stop");
        break;
    case "Yello":
        console.log("Slow down");
        break;
    case "Green":
        console.log("Go");
        break;
    default:
        console.log("Signal is broken");
}